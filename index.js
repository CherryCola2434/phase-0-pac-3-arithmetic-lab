function add(a, b) {
  return a + b;
}
function subtract(a, b) {
  return a - b;
}
function multiply(a, b) {
  return a * b;
}
function divide(a, b) {
  return a / b;
}
function makeInt(number= 10) {
  return parseInt(number, 10);
  }
function preserveDecimal(number){
  return parseFloat(number);
}
function increment(number= 5) {
  return number += 1;
}
function decrement(number= 5) {
  return number -= 1;
}





